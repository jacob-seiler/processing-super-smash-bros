var projectiles = [undefined, undefined, undefined, undefined];
// Projectile function
var projectile = function(player, x, y, type, size, direction) {
    this.player = player;
    this.x = x;
    this.y = y;
    this.type = type;
    this.size = size;
    this.direction = direction;
    
    projectiles[player] = this;
};
// Draws the projectile
projectile.prototype.draw = function() {
    pushMatrix();
    
    if (this.direction === LEFT) {
        translate(this.x - (30 + this.size), this.y);
        rotate(180);
    } else {
        translate(this.x, this.y);
    }
    
    if (this.type === 1) {
        var s = 25 + this.size;
        fill(255, 0, 0);
        ellipse(0, 0, s + 5, s);
        fill(255, 111, 0);
        ellipse(0, 0, (s + 5) / 1.5, s / 1.5);
        fill(255, 221, 0);
        ellipse(0, 0, (s + 5) / 2, s / 2);
    } else if (this.type === 2) {
        var s = 30 + this.size;
        fill(120, 90, 50);
        stroke(120, 90, 50);
        strokeWeight(s / 10);
        line(0 - (s / 3), 0, 0 - s, 0);
        noStroke();
        fill(255, 255, 255);
        triangle(0, 0, 0 - (s / 3), 0 + (s / 6), 0 - (s / 3), 0 - (s / 6));
    } else if (this.type === 4) {
        var s = 25 + this.size;
        fill(65, 130, 200);
        ellipse(0, 0, s, s);
        fill(0, 217, 255);
        ellipse(0, 0, (s / 1.5), (s / 1.5));
    } else if (this.type === 5) {
        var s = 25 + this.size;
        fill(175, 175, 175);
        ellipse(0, 0, s, s);
        fill(160, 160, 160);
        ellipse(0, 0, (s / 1.5), (s / 1.5));
    } else if (this.type === 6) {
        var s = 30 + this.size;
        fill(120, 90, 50);
        stroke(120, 90, 50);
        strokeWeight(s / 10);
        line(0 - (s / 3), 0, 0 - s, 0);
        noStroke();
        fill(255, 255, 255);
        triangle(0, 0, 0 - (s / 3), 0 + (s / 6), 0 - (s / 3), 0 - (s / 6));
    } else if (this.type === 7) {
        var s = 20 + this.size;
        fill(65, 130, 200);
        ellipse(0, 0, s, s);
        fill(0, 217, 255);
        ellipse(0, 0, (s / 1.6), (s / 1.6));
    }
    popMatrix();
};
// Moves the projectile
projectile.prototype.move = function(speed) {
    if ((this.direction === RIGHT && this.x > (430 + this.size)) || (this.direction === LEFT && this.x < (-30 - this.size))) {
        projectiles[this.player] = undefined;
    } else {
        var dir = 1;
        if (this.direction === LEFT) {
            dir = -1;
        }
        
        this.x = this.x + (speed * dir);
    }
};
// Checks if the projectile is colliding with another player
projectile.prototype.collision = function(x, y, width, height) {
    if (this.x + (this.size / 2) > x && this.x - (this.size / 2) < (x + width) && this.y + (this.size / 2) > y && this.y - (this.size / 2) < (y + height)) {
        return true;
    }
    
    return false;
};


